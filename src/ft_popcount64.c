#include "libft.h"

/*
** M1:	0x5555555555555555	0101...
** M2:  0x3333333333333333	00110011...
** M4:	0x0f0f0f0f0f0f0f0f	0000111100001111...
*/

int	ft_popcount64(uint64_t x)
{
	x -= (x >> 1) & M1;
	x = (x & M2) + ((x >> 2) & M2);
	x = (x + (x >> 4)) & M4;
	x += x >> 8;
	x += x >> 16;
	x += x >> 32;
	return (x & 0x7f);
}
