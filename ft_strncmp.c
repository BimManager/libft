int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
		return (0);
	while (--n)
	{
		if (*s1 == *s2)
		{
			++s1;
			++s2;
			continue;
		}
		return (*s1 - *s2);
	}
	return (*s1 - *s2);
}
